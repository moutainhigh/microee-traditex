package com.microee.traditex.liqui.app.props.conf;

public class HBiTexAccountConf {

    private String exchangeCode;
    private String uid;
    private String spotId;
    private String wshost;
    private String resthost;
    private String dawnhost;
    private String accessKey;
    private String secretKey;

    public HBiTexAccountConf() {

    }

    public String getExchangeCode() {
        return exchangeCode;
    }

    public void setExchangeCode(String exchangeCode) {
        this.exchangeCode = exchangeCode;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getSpotId() {
        return spotId;
    }

    public void setSpotId(String spotId) {
        this.spotId = spotId;
    }

    public String getWshost() {
        return wshost;
    }

    public void setWshost(String wshost) {
        this.wshost = wshost;
    }

    public String getResthost() {
        return resthost;
    }

    public void setResthost(String resthost) {
        this.resthost = resthost;
    }

    public String getDawnhost() {
        return dawnhost;
    }

    public void setDawnhost(String dawnhost) {
        this.dawnhost = dawnhost;
    }

    public String getAccessKey() {
        return accessKey;
    }

    public void setAccessKey(String accessKey) {
        this.accessKey = accessKey;
    }

    public String getSecretKey() {
        return secretKey;
    }

    public void setSecretKey(String secretKey) {
        this.secretKey = secretKey;
    }

}
